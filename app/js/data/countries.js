/** Get country name from ISO code */
export function getCountryName(code) {
    if (!code) return '';
    const country = COUNTRIES.find(c => c.code === code.toUpperCase());
    return country ? country.fr : code;
}

/** Fuzzy search countries (accent-insensitive, starts-with first) */
export function searchCountries(query, limit = 8) {
    if (!query || query.length < 2) return [];

    const normalized = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');

    const results = COUNTRIES.filter(c => {
        const name = c.fr.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        return name.includes(normalized) || c.code.toLowerCase() === normalized;
    });

    results.sort((a, b) => {
        const nameA = a.fr.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        const nameB = b.fr.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        const startsA = nameA.startsWith(normalized);
        const startsB = nameB.startsWith(normalized);
        if (startsA && !startsB) return -1;
        if (!startsA && startsB) return 1;
        return nameA.localeCompare(nameB, 'fr');
    });

    return results.slice(0, limit);
}
